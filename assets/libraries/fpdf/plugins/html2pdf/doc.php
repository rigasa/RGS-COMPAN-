<h2>Description</h2>
Cette classe convertit de l'HTML en PDF. Elle a été conçue pour exporter mes blogs en PDF.<br>
Les principales fonctionnalités sont :
<ul>
	<li>support du gras, italique et souligné</li>
	<li>support de &lt;h1&gt;, &lt;h2&gt;, &lt;h3&gt; et &lt;h4&gt;</li>
	<li>support des images</li>
	<li>support de base pour les tables</li>
	<li>support des listes &lt;li&gt;</li>
	<li>support de &lt;blockquote&gt;</li>
	<li>support des pseudo-tags &lt;red&gt; and &lt;blue&gt;</li>
	<li>&lt;pre&gt; s'affiche en Courier, le reste en Times</li>
	<li>support de &lt;br&gt;, &lt;br /&gt; et &lt;p&gt;</li>
	<li>support de &lt;hr&gt; et &lt;hr /&gt;</li>
	<li>l'en-tête du document est créé avec le titre de l'article, l'URL, l'auteur et la date de publication</li>
	<li>le pied contient le numéro de page</li>
</ul>
